#!/usr/bin/python3

def add(a, b):
    """ addition function

    Arguments:
        a: first number
        b: second number

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """ subtraction function

    Arguments::
        a: first number
        b: second number

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """ multiplication function

    Arguments::
        a: first number
        b: second number

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """ division function

    Arguments::
        a: first number
        b: second number

    Returns:
        The return value. a / b
    """
    return int(a / b)
